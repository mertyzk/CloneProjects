import Foundation
import UIKit

extension UIColor {
    
    static func rgb(red : CGFloat, green : CGFloat, blue : CGFloat) -> UIColor {
        return UIColor(red: red/255, green: green/255, blue: green/255, alpha: 1)
    }
    
}
